class Solution {
    static bool cmp(int a, int b) {
        string _a = to_string(a), _b = to_string(b);
        return _a + _b < _b + _a;
    }
public:
    string minNumber(vector<int>& nums) {
        sort(nums.begin(), nums.end(), cmp);
        string res;
        for (int num : nums) {
            res += to_string(num);
        }
        return res;
    }
};
