class Solution {
public:
    int findLength(vector<int>& nums1, vector<int>& nums2) {
        vector<int> dp(nums2.size() + 1, 0);
        int res = 0;
        for (int i = 1; i <= nums1.size(); i ++) {
            for (int j = nums2.size(); j != 0; j --) {
                if (nums2[j - 1] == nums1[i - 1]) {
                    dp[j] = dp[j - 1] + 1;
                } else {
                    dp[j] = 0;
                }
                res = max(res, dp[j]);
            }
        }
        return res;
    }
};
