class Solution {
private:
    vector<vector<string>> res{};
    void putOne(vector<pair<int, int>> &used, int row, int n) {
        if (used.size() == n) {
            vector<string> tmp{};
            for (auto point : used) {
                string a(n, '.');
                a[point.second] = 'Q';
                tmp.emplace_back(a);
            }
            res.emplace_back(tmp);
        }

        for (int i = 0; i < n; i ++) {
            bool flag = false;
            for (auto point : used) {
                if (point.second == i || abs(point.first - row) == abs(point.second -  i)) {
                    flag = true;
                    break;
                }
            }
            if (flag) {
                continue;
            }
            used.emplace_back(row, i);
            putOne(used, row + 1, n);
            used.pop_back();
        }
    }
public:
    vector<vector<string>> solveNQueens(int n) {
        vector<pair<int, int>> used{};
        putOne(used, 0, n);
        return res;
    }
};
