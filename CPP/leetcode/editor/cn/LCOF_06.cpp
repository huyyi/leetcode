/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    vector<int> reversePrint(ListNode* head) {
        int length = 0;
        auto cur = head;
        while (cur != nullptr) {
            length ++;
            cur = cur->next;
        }
        vector<int> res(length, 0);
        cur = head;
        while (cur != nullptr) {
            length --;
            res[length] = cur->val;
            cur = cur->next;
        }
        return res;
    }
};
