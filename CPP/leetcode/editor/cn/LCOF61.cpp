class Solution {
public:
    bool isStraight(vector<int>& nums) {
        int mi = -1, mx = -1, z = 0;
        vector<bool> a(14, false);
        for (int num : nums) {
            if (num == 0) {
                z ++;
                continue;
            }
            if (mi == -1 || num < mi) mi = num;
            if (mx == -1 || num > mx) mx = num;
            if (mx - mi > 5) return false;
            if (a[num]) return false;
            a[num] = true;
        }
        return mx - mi > 5;
    }
};
