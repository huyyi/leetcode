class Solution {
public:
    bool wordBreakMy(const string& s, vector<string>& wordDict) {
        vector<bool> dp(s.size()+1, false);
        dp[0] = true;
        for (int i = 1; i <= s.size(); i ++) {
            for (const string &str : wordDict) {
                if (i >= str.size() && str == s.substr(i - str.size(), str.size())) {
                    dp[i] = dp[i] || dp[i - str.size()] ;
                }
            }
        }
        return dp[s.size()];
    }

    bool wordBreak(string s, vector<string>& wordDict) {
        unordered_set<string> wordSet(wordDict.begin(), wordDict.end());
        vector<bool> dp(s.size() + 1, false);
        dp[0] = true;
        for (int i = 1; i <= s.size(); i++) {   // éå†èƒŒåŒ…
            for (int j = 0; j < i; j++) {       // éå†ç‰©å“
                string word = s.substr(j, i - j); //substr(èµ·å§‹ä½ç½®ï¼Œæˆªå–çš„ä¸ªæ•°)
                if (wordSet.find(word) != wordSet.end() && dp[j]) {
                    dp[i] = true;
                }
            }
        }
        return dp[s.size()];
    }
};
