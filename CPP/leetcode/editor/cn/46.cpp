class Solution {
    vector<vector<int>> res;
    vector<int> path;
    vector<bool> used;
    int n;
public:
    vector<vector<int>> permute(vector<int>& nums) {
        used = vector<bool>(nums.size(), false);
        n = nums.size();
        process(nums);
        return res;
    }

    void process(const vector<int> &nums) {
        if (path.size() == n) res.emplace_back(path);
        else {
            for (int i = 0; i < n; ++i) {
                if (!used[i]) {
                    path.push_back(nums[i]);
                    used[i] = true;
                    process(nums);
                    used[i] = false;
                    path.pop_back();
                }
            }
        }
    }
};
